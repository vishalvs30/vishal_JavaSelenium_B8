package assignments12;

public class Bitwiseop {

	public static void main(String[] args) {

		int a = 5, b = 3;
        System.out.println("AND: " + (a & b)); 
        System.out.println("OR: " + (a | b));  
        System.out.println("XOR: " + (a ^ b)); 
        System.out.println("NOT A: " + (~a)); 
	}

}
