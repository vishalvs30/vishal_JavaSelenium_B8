package assignments12;

public class operators {
	public static void main(String[] args) {
        int a = 10, b = 20;
        System.out.println("Addition: " + (a + b));      // Arithmetic
        System.out.println("Equal: " + (a == b));         // Relational
        System.out.println("Logical AND: " + (a < b && b > 10)); // Logical
    }
}
