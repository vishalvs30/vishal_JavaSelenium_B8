package assignments12;

public class Casting {

	public static void main(String[] args) {

		    int i = 10;
	        double d = i;
	        System.out.println("Implicit Casting: " + d);

	  
	        double x = 9.7;
	        int y = (int) x;
	        System.out.println("Explicit Casting: " + y);
		
	}

}
